
public class Servidor {

    private int id_matricula ;
    private  String nom_servidor  ;
    private  String cpf_servidor ;
    private  String sigla_uf ;
    private  double val_salario ;
    private  String id_sigla ;
    private  String cargo  ;




    public int getId_matricula() {
        return id_matricula;
    }
    public void setId_matricula(int id_matricula) {
        this.id_matricula = id_matricula;
    }
    public String getNom_servidor() {
        return nom_servidor;
    }
    public void setNom_servidor(String nom_servidor) {
        this.nom_servidor = nom_servidor;
    }
    public String getCpf_servidor() {
        return cpf_servidor;
    }
    public void setCpf_servidor(String cpf_servidor) {
        this.cpf_servidor = cpf_servidor;
    }
    public String getSigla_uf() {
        return sigla_uf;
    }
    public void setSigla_uf(String sigla_uf) {
        this.sigla_uf = sigla_uf;
    }
    public double getVal_salario() {
        return val_salario;
    }
    public void setVal_salario(double val_salario) {
        this.val_salario = val_salario;
    }
    public String getId_sigla() {
        return id_sigla;
    }
    public void setId_sigla(String id_sigla) {
        this.id_sigla = id_sigla;
    }
    public String getCargo() {
        return cargo;
    }
    public void setCargo(String cargo) {
        this.cargo = cargo;
    }


}
